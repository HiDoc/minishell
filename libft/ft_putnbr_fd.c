#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nb;

	nb = 0;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		nb = n * -1;
		if (n == -2147483648)
		{
			nb = 2147483648;
		}
	}
	else
	{
		nb = n;
	}
	if (nb / 10 > 0)
	{
		ft_putnbr_fd(nb / 10, fd);
	}
	ft_putchar_fd((nb % 10) + 48, fd);
}
